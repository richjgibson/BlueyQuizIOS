    @State private var correctAnswer: Int = Int.random(in: 0..<3)
    @State private var playerScore: Int = 0
    @State private var showAlert: Bool = false
    @State private var alertString: String = ""
    
    func guess(_ number: Int) {
        if(number == correctAnswer) {
            playerScore += 1
            resetQuestion()
            if(playerScore == 10) {
                alertString = "You win!"
                showAlert = true
            }
        } else {
            alertString = "You lose"
            playerScore = 0
            showAlert = true
        }
    }
    
    func resetQuestion() {
        episodeList.shuffle()
        correctAnswer = Int.random(in: 0..<3)
    }
    
    func getAnswerString() -> String {
        let fullString = episodeList[correctAnswer]
        if let underscoreIndex = fullString.firstIndex(of: "_") {
            return String(fullString[..<underscoreIndex])
        } else {
            return fullString
        }
    }
    
    func resetQuiz() {
        playerScore = 0
        resetQuestion()
    }
    
